<h3>What is True Dungeon?</h3>

<p>True Dungeon (copyright True Adventures, Ltd.) is a real-life dungeon crawl currently
held at the GenCon gaming convention every year in Indianapolis, IN. In True Dungeon,
parties of up to 10 adventurers enter various dungeons trying to solve puzzles, defeat
 monsters, and of course find treasure. Treasure and equipment are represented by 
tokens (very much like poker chips) which you can purchase directly from True Adventures
Ltd,, receive as treasure in the dungeon, and of course buy here. The tokens are
collectible which means they generally come in packs with random distributions and
when the current year's tokens sell out they may not be printed again.</p>

